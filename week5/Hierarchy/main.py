from player import Player, AudioPlayer, VideoPlayer, DvdPlayer

if __name__ == "__main__":
    print("=== Тестируем базовый плеер ===")
    player = Player("Трек №1")
    player.запустить()
    player.остановить()

    print("\n=== Тестируем аудиоплеер ===")
    audio = AudioPlayer("Альбом 'Музыка'", "mp3", 256)
    audio.запустить()
    audio.set_format("wav")
    audio.остановить()

    print("\n=== Тестируем видеоплеер ===")
    video = VideoPlayer("Фильм 'Приключения'", "1080p", 24)
    video.запустить()
    video.set_resolution("4K")
    video.остановить()

    print("\n=== Тестируем DVD-плеер ===")
    dvd = DvdPlayer("Фильм 'Классика'", "1080p", "Disk 1", 2)
    dvd.запустить()
    dvd.set_disk("Disk 2")
    dvd.остановить()

